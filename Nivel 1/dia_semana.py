numSemana = int(input("Introduce un número de 1 al 7: "))
if numSemana == 1:
    print("Es lunes")
elif numSemana == 2:
    print("Es martes")
elif numSemana == 3:
    print("Es miércoles")
elif numSemana == 4:
    print("Es jueves")
elif numSemana == 5:
    print("Es viernes")
elif numSemana == 6:
    print("Es sábado")
else:
    print("Es domingo")